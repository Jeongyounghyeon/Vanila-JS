const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  sub: function (a, b) {
    console.log(a - b);
  },
  mul: function (a, b) {
    console.log(a * b);
  },
  div: function (a, b) {
    console.log(a / b);
  },
  squ: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(1, 2);
calculator.sub(3, 4);
calculator.mul(5, 6);
calculator.div(7, 8);
calculator.squ(9, 10);
